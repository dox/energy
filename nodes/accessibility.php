<h3>Technical Information about this website&rsquo;s accessibility</h3>
<p>St Edmund Hall is committed to making its website accessible, in accordance with the Public Sector Bodies (Websites and Mobile Applications) (No. 2) Accessibility Regulations 2018.</p>

<h3>Compliance Status</h3>
<p>This website is partially compliant with the&nbsp;<a href="https://www.w3.org/TR/WCAG21/">Web Content Accessibility Guidelines version 2.1</a>&nbsp;AA standard due to the non-compliances listed below.</p>

<h3>Non-accessible content</h3>
<p>St Edmund Hall&rsquo;s IT Manager conducted a web accessibility audit in July 2020. The <a href="https://www.gov.uk/government/publications/doing-a-basic-accessibility-check-if-you-cant-do-a-detailed-one/doing-a-basic-accessibility-check-if-you-cant-do-a-detailed-one">UK Government&rsquo;s basic accessibility checklist</a> was used in conjunction with the free <a href="https://chrome.google.com/webstore/detail/siteimprove-accessibility/efcfolpjihicnikpmhnmphjhhpiclljc">Site Improve Accessibility Checker Chrome Extension</a>. The code was modified to comply with the new standards, however, there remains a few errors which can be justified within the context of the site.</p>
<p>The content listed below is non-accessible for the following reasons.</p>

<h3>Non-compliance with the accessibility regulations</h3>
<p>Some non-text content is not labelled. This fails WCAG 1.1.1 Non-text Content</p>
<p>Small tags are used to alter text size. This fails WCAG 1.3.1 Info and Relationships.</p>
<p>Link identified only by colour. This fails WCAG 1.4.1 Use of Colour.</p>
<p>Colour contrast is insufficient. This fails WCAG AA 1.4.3 Contrast (Minimum).</p>
<p>Some of our images contain text. This fails WCAG AA 1.4.5 Images of Text.</p>
<p>No option to skip repeated content. This fails WCAG A 2.4.1 Bypass Blocks.</p>
<p>Headings are not hierarchically organised. This fails WCAG AAA 2.4.10 Section Headings.</p>

